class LevelManager:
    """关卡管理器"""

    def __init__(self):
        self.current_level = 1
        self.terrain_manager = TerrainManager()
        self.enemies_remaining = 0
        self.enemy_spawn_timer = 0
        self.current_enemy_config = []
        self.enemy_config_index = 0
        self.enemies_spawned = 0
        self.spawn_points = [
            (0, 0),  # 左上
            (SCREEN_WIDTH // 2 - TILE_SIZE // 2, 0),  # 中上
            (SCREEN_WIDTH - TILE_SIZE, 0),  # 右上
        ]

    def load_level(self, level_num):
        """加载关卡"""
        if level_num < 1 or level_num > len(LEVEL_MAPS):
            return False

        self.current_level = level_num
        map_data = LEVEL_MAPS[level_num - 1]
        self.terrain_manager.load_from_grid(map_data)

        # 设置敌人配置
        self.current_enemy_config = LEVEL_ENEMIES[level_num - 1]
        self.enemy_config_index = 0
        self.enemies_spawned = 0
        self.enemies_remaining = sum(config[1] for config in self.current_enemy_config)
        self.enemy_spawn_timer = 0

        return True

    def get_player_start_position(self, player_id=1):
        """获取玩家起始位置"""
        if player_id == 1:
            # 玩家 1 在基地左边
            base_x = (GRID_WIDTH - 3) * TILE_SIZE // 2
            base_y = (GRID_HEIGHT - 2) * TILE_SIZE
            return (base_x - TILE_SIZE * 2, base_y)
        else:
            # 玩家 2 在基地右边
            base_x = (GRID_WIDTH - 3) * TILE_SIZE // 2
            base_y = (GRID_HEIGHT - 2) * TILE_SIZE
            return (base_x + TILE_SIZE * 2, base_y)

    def get_base_position(self):
        """获取基地位置"""
        base_x = (GRID_WIDTH - 3) * TILE_SIZE // 2
        base_y = (GRID_HEIGHT - 1) * TILE_SIZE
        return (base_x, base_y)

    def update(self):
        """更新关卡状态"""
        self.terrain_manager.update()

        # 更新敌人生成
        if self.enemies_spawned < sum(config[1] for config in self.current_enemy_config):
            self.enemy_spawn_timer += 1

            if self.enemy_config_index < len(self.current_enemy_config):
                enemy_type, enemy_count, spawn_interval = self.current_enemy_config[self.enemy_config_index]

                if self.enemy_spawn_timer >= spawn_interval:
                    self.enemy_spawn_timer = 0

                    # 检查是否还有位置生成敌人
                    spawn_point = self.get_spawn_point()
                    if spawn_point:
                        self.enemies_spawned += 1
                        return (enemy_type, spawn_point[0], spawn_point[1])

                    # 检查当前配置是否完成
                    spawned_for_current = sum(
                        1 for c in self.current_enemy_config[:self.enemy_config_index]
                        for _ in range(c[1])
                    ) + self.enemies_spawned

        return None

    def get_spawn_point(self):
        """获取敌人生成点"""
        # 简单实现，返回一个可用的生成点
        import random
        return random.choice(self.spawn_points)

    def is_level_complete(self):
        """检查关卡是否完成"""
        return self.enemies_spawned >= sum(config[1] for config in self.current_enemy_config) and \
               self.enemies_remaining <= 0

    def enemy_defeated(self):
        """敌人被击败"""
        if self.enemies_remaining > 0:
            self.enemies_remaining -= 1

    def get_next_level(self):
        """获取下一关"""
        if self.current_level < len(LEVEL_MAPS):
            return self.current_level + 1
        return None


